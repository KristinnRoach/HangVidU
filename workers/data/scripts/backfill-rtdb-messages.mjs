#!/usr/bin/env node
// One-off: convert an RTDB `conversations` export into an idempotent SQL file
// that backfills D1 (0001 + 0002 schema). Direct conversations only; lossy by
// design (legacy inline-file rows and non-R2 attachments are dropped, matching
// the messaging-next RTDB adapter's `toIncoming`).
//
// Input  : JSON dump of the RTDB `/conversations` node:
//            { "<a_b>": { "messages": { "<key>": {...} }, ... }, ... }
// Output : SQL applied with
//            wrangler d1 execute hangvidu-data --local  --file=<out> --config workers/data/wrangler.jsonc
//            wrangler d1 execute hangvidu-data --remote --file=<out> --config workers/data/wrangler.jsonc
//
// Idempotent: re-running inserts nothing new. Conversation ids are resolved in
// SQL via `WHERE dm_key=?`, so an existing DM (created by the live app) is
// reused rather than duplicated.
//
// Usage: node backfill-rtdb-messages.mjs --input rtdb-conversations.json --out backfill-messages.sql

import { readFileSync, writeFileSync } from 'node:fs';
import { randomUUID } from 'node:crypto';

function arg(name, fallback) {
  const i = process.argv.indexOf(name);
  return i !== -1 && process.argv[i + 1] ? process.argv[i + 1] : fallback;
}

const inputPath = arg('--input', 'rtdb-conversations.json');
const outPath = arg('--out', 'backfill-messages.sql');

/** SQL string literal with single-quote escaping; null/undefined -> NULL. */
function s(value) {
  if (value === null || value === undefined) return 'NULL';
  return `'${String(value).replaceAll("'", "''")}'`;
}
/** SQL integer literal; null/undefined -> NULL. */
function n(value) {
  return typeof value === 'number' && Number.isFinite(value) ? String(Math.trunc(value)) : 'NULL';
}

function nonEmptyString(value) {
  return typeof value === 'string' && value.trim() !== '';
}

// Mirror of the messaging-next RTDB adapter `toIncoming` shape decisions.
function normalizeMessage(raw) {
  if (!raw || typeof raw !== 'object' || !raw.from) return null;
  const from = String(raw.from);
  const sentAt = typeof raw.sentAt === 'number' ? raw.sentAt : null;
  if (sentAt === null) return null; // need a real timestamp for created_at ordering

  if (raw.type === 'file') {
    const st = raw.storage && typeof raw.storage === 'object' ? raw.storage : null;
    const r2 =
      st && st.provider === 'r2' && nonEmptyString(st.bucket) && nonEmptyString(st.key);
    if (
      !r2 ||
      !nonEmptyString(raw.fileName) ||
      !nonEmptyString(raw.mimeType) ||
      typeof raw.fileSize !== 'number' ||
      !Number.isFinite(raw.fileSize) ||
      raw.fileSize <= 0
    ) {
      return null; // legacy inline / malformed file row — dropped (lossy, OK)
    }
    return {
      from,
      sentAt,
      kind: 'file',
      body: typeof raw.text === 'string' && raw.text !== '' ? raw.text : null,
      attachment: {
        bucket: raw.storage.bucket,
        key: raw.storage.key,
        fileName: raw.fileName,
        mimeType: raw.mimeType,
        fileSize: raw.fileSize,
      },
    };
  }

  if (typeof raw.text !== 'string') return null;
  return { from, sentAt, kind: 'text', body: raw.text, attachment: null };
}

const root = JSON.parse(readFileSync(inputPath, 'utf8'));
const conversations = root?.conversations ?? root; // tolerate either {conversations:{}} or {}

const lines = [];
lines.push('-- Generated by backfill-rtdb-messages.mjs. Idempotent; safe to re-run.');
lines.push('-- Direct conversations only; non-R2/inline file rows dropped.');
lines.push('');

const stats = { convos: 0, skippedGroup: 0, skippedEmpty: 0, text: 0, file: 0, dropped: 0 };

for (const [convoId, convo] of Object.entries(conversations ?? {})) {
  const parts = convoId.split('_');
  if (parts.length !== 2 || !parts[0] || !parts[1]) {
    stats.skippedGroup += 1; // group:/opaque/malformed — not a legacy direct id
    continue;
  }
  const [a, b] = parts;
  const dmKey = [a, b].sort().join(':');
  const dmKeyLit = s(dmKey);

  const rawMessages = convo?.messages ?? {};
  const msgs = [];
  for (const [key, raw] of Object.entries(rawMessages)) {
    const m = normalizeMessage(raw);
    if (m) msgs.push({ key, ...m });
    else stats.dropped += 1;
  }
  if (msgs.length === 0) {
    stats.skippedEmpty += 1;
    continue;
  }

  const minTs = Math.min(...msgs.map((m) => m.sentAt));
  const maxTs = Math.max(...msgs.map((m) => m.sentAt));
  const newConvoId = randomUUID();
  const senderIds = Array.from(new Set(msgs.map((m) => m.from)));

  lines.push(`-- conversation ${convoId} (${msgs.length} messages)`);
  for (const uid of new Set([a, b, ...senderIds])) {
    lines.push(
      `INSERT OR IGNORE INTO users (id, display_name, created_at) VALUES (${s(uid)}, NULL, ${n(minTs)});`,
    );
  }
  lines.push(
    `INSERT OR IGNORE INTO conversations (id, kind, dm_key, created_at, updated_at) VALUES (${s(newConvoId)}, 'direct', ${dmKeyLit}, ${n(minTs)}, ${n(maxTs)});`,
  );
  lines.push(
    `UPDATE conversations SET updated_at = MAX(updated_at, ${n(maxTs)}) WHERE dm_key=${dmKeyLit};`,
  );
  for (const uid of [a, b]) {
    lines.push(
      `INSERT OR IGNORE INTO conversation_members (conversation_id, user_id, joined_at) SELECT id, ${s(uid)}, ${n(minTs)} FROM conversations WHERE dm_key=${dmKeyLit};`,
    );
  }

  for (const m of msgs) {
    lines.push(
      `INSERT OR IGNORE INTO messages (id, conversation_id, sender_id, kind, body, created_at) SELECT ${s(m.key)}, id, ${s(m.from)}, ${s(m.kind)}, ${s(m.body)}, ${n(m.sentAt)} FROM conversations WHERE dm_key=${dmKeyLit};`,
    );
    if (m.kind === 'file') stats.file += 1;
    else stats.text += 1;

    if (m.attachment) {
      const at = m.attachment;
      lines.push(
        `INSERT INTO message_attachments (id, message_id, r2_key, bucket, file_name, mime_type, file_size, width, height) SELECT ${s(randomUUID())}, ${s(m.key)}, ${s(at.key)}, ${s(at.bucket)}, ${s(at.fileName)}, ${s(at.mimeType)}, ${n(at.fileSize)}, NULL, NULL WHERE NOT EXISTS (SELECT 1 FROM message_attachments WHERE message_id=${s(m.key)});`,
      );
    }
  }
  lines.push('');
  stats.convos += 1;
}

writeFileSync(outPath, lines.join('\n'));
console.error(
  `Wrote ${outPath}\n  conversations: ${stats.convos}\n  messages: ${stats.text} text, ${stats.file} file\n  dropped messages: ${stats.dropped}\n  skipped convos: ${stats.skippedGroup} non-direct-id, ${stats.skippedEmpty} empty`,
);
